package entidade;

/**
 *
 * @author darie
 */
public class Setor {

    private int id;
    private String nome;
    private char status;

    public Setor() {
        id = 0;
        nome = "";
        status = 'A';
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public char getStatus() {
        return status;
    }

    public void setStatus(char status) {
        this.status = status;
    }

}
